    <p>
      <b>Identify and add to the corpus those papers using a literature search on the nature of creativity, using the Scopus database</b>
    </p>
    <ul>
      <li>
        Papers should be available as full text, in English, and in PDF form or other format which it is straightforward to extract text from
      </li>
      <li>
        Choose papers according to the following inclusion criteria (in order of descending precedence):

        <ul>
          <li>
            Paper content should primarily focus on discussion of the nature of creativity

            <ul>
              <li>
                Use titles and abstracts to identify candidates
              </li>
              <li>
                Check content of candidate paper to refine by content
              </li>
            </ul>
          </li>
          <li>
            Paper should be particularly influential

            <ul>
              <li>
                Compare citation counts, preferring papers with higher citations than their disciplinary/time-based contemporaries
              </li>
              <li>
                For the most recent papers (less time to accrue citations) make subjective judgement of influence based on a knowledge of the field
              </li>
            </ul>
          </li>
          <li>
            Give preference to a selection of papers (where reasonable) which represent a cross-section of years since 1950
          </li>
          <li>
            Give preference to a selection of papers (where reasonable) which represent a cross-section of disciplines relevant to the discussion of the nature of creativity
          </li>
        </ul>
      </li>
      <li>
        Exclude papers where the author is already represented in the corpus (using the most influential/highly cited paper from that author in the corpus, as judged above) - unless:

        <ul>
          <li>
            The two papers address different perspectives on creativity
          </li>
        </ul>
      </li>
    </ul>
